/* 
- Create variables a and b, and assign them integer values.
- Use add, subtraction, multiply, divide, and modulus operator for a and b. Store each result in separate variables (sum, difference, product, quotient, reminder).

- Use console.log() to print the results of each operation to the console.

- Add comments in your code to explain what each operation does. */


let a = 15, b = 4; // Initialized variables and assigned integer values

let sum, difference, product, quotient, reminder; // Initialized variables to store and print the results

sum = a + b; // adds two variables
difference = a - b; // substracts two variables
product = a * b; // multiply two variables
quotient = a / b; // divides two variables
reminder = a % b; // finds out the remainder using modulus operator

// Printing the results below
console.log('Sum =', sum, '\n');
console.log('Difference =', difference, '\n');
console.log('Product =', product, '\n');
console.log('Quotient =', quotient, '\n');
console.log('Remainder =', reminder, '\n');
